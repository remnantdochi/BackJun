#include <stdio.h>
#include <stdlib.h>

void brute(int* list, int n, int focus) {
	int i, j;
	int temp;

	if (n - 1 == focus) {  //  focus가 마지막에 도착하면 출력
		for (i = 0; i < n; i++) {
			printf("%d ", list[i]);
		}
		printf("\n");
	}
	else {  //  아직 끝에 도달하지 못했으면
		for (i = focus; i < n; i++) {
			///////////////////////////////////////////////////
			//  focus 위치에 뒷쪽 숫자들을 순서대로 넣어봄
			///////////////////////////////////////////////////
			temp = list[i];
			for (j = i ; j > focus; j--) {
				list[j] = list[j - 1];
			}
			list[focus] = temp;

			///////////////////////////////////////////////////
			//  다음 단계
			///////////////////////////////////////////////////
			brute(list, n, focus + 1);

			///////////////////////////////////////////////////
			//  원상 복구
			///////////////////////////////////////////////////
			temp = list[focus];
			for (j = focus; j < i; j++) {
				list[j] = list[j + 1];
			}
			list[i] = temp;
		}
	}

	return 0;
}

int main() {
	int n;
	int i;
	int* list;

	///////////////////////////////////////////////////
	//  크기 입력
	///////////////////////////////////////////////////
	scanf("%d", &n);

	///////////////////////////////////////////////////
	//  수열 할당 및 초기화
	///////////////////////////////////////////////////
	list = (int*)malloc(sizeof(int) * n);
	for (i = 0; i < n; i++) {
		list[i] = i + 1;
	}

	///////////////////////////////////////////////////
	//  brutal 시작
	///////////////////////////////////////////////////
	brute(list, n, 0);

	return 0;
}
