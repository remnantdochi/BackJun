#include <stdio.h>
#include <stdlib.h>

//////////////////////////////////////////
// n+1자릿수 좋은수열은 n자릿수에 하나 더해진 형태와 동일
//////////////////////////////////////////
int brute(int* list, int n, int focus, int end) {
	int check = 1;
	int i, j, k;

	//////////////////////////////////////////
	// 순열 검사
	//////////////////////////////////////////
	for (i = 1; i * 2 <= focus ; i++) {
		check = 0;
		for (j = 0; j < i; j++) {  //맨끝 포함 안 된 부분은 이미 검사 했으므로
			if (list[focus - j - 1] != list[focus - i - j - 1]) {  //맨끝에 추가된부분만 검사
				check = 1;
			}
		}
		if (check == 0) {
			return 0;
		}
	}

	//////////////////////////////////////////
	// 검사통과 후 n자릿수 완성했으면 1을 반환하여 종료를 알림
	//////////////////////////////////////////
	if (focus == n) {
		return 1;
	}

	//////////////////////////////////////////
	// 아니면 다음단계 진입
	//////////////////////////////////////////
	else {
		for (i = 1; i <= 3; i++) {
			list[focus] = i;  //1, 2, 3 시도
			end = brute(list, n, focus + 1, end);  //다음단계
			if (end) {  // 끝났으면 1 반환하여 종료
				return 1;
			}
		}
	}
	return 0;
}

int main() {
	int n;
	int i;
	int* list;
	
	//////////////////////////////////////////
	// 변수 입력
	//////////////////////////////////////////
	scanf("%d", &n);

	//////////////////////////////////////////
	// 순열 할당
	//////////////////////////////////////////
	list = (int*)malloc(sizeof(int) * n);

	//////////////////////////////////////////
	// brutal 시작
	//////////////////////////////////////////
	brute(list, n, 0, 0);

	//////////////////////////////////////////
	// 결과 출력
	//////////////////////////////////////////
	for (i = 0; i < n; i++) {
		printf("%d", list[i]);
	}

	return 0;
}
