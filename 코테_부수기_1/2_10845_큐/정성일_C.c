#include <stdio.h>
#include <string.h>  //strcmp 사용 목적

int main() {
	int cmd_num, cmd_ln; //커맨드 갯수 카운트 변수
	int temp, i;  //for문용 변수
	int m_max = 0; //크기 저장 변수
	int m_que[10000] = { .0 }; //큐
	char cmd_ip[6]; //커맨드 저장 - 가장 긴 커맨드가 5글자이므로 \0까지 6개의 배열

	scanf("%d", &cmd_num); //커맨드 갯수 입력

	for (cmd_ln = 0; cmd_ln < cmd_num; cmd_ln++) {  //커맨드 반복 입력
		cmd_ip[0] = ' ';
		cmd_ip[1] = ' ';
		cmd_ip[2] = ' ';
		cmd_ip[3] = ' ';
		cmd_ip[4] = ' ';
		cmd_ip[5] = ' ';  //커맨드 스트링 초기화
		scanf("%s", cmd_ip);  //커맨드 입력

		///////////////////////////////////////////////////////////////////////////////////////
		// push
		///////////////////////////////////////////////////////////////////////////////////////
		if (strcmp(cmd_ip, "push") == 0) {
			scanf("%d", &m_que[m_max]);  //정수 입력 동시에 큐에 추가
			m_max++;  //크기 증가
		}

		///////////////////////////////////////////////////////////////////////////////////////
		// pop
		///////////////////////////////////////////////////////////////////////////////////////
		if (strcmp(cmd_ip, "pop") == 0) {
			if (m_max == 0) {  //비어있으면
				printf("-1\n");
			}
			else {
				printf("%d\n", m_que[0]);  //맨앞 출력
				for (i = 0; i < m_max; i++) {  //큐 앞으로 밀기
					m_que[i] = m_que[i + 1];
				}
				m_que[m_max - 1] = 0;  //맨뒤 큐 초기화
				m_max--;  //크기 감소
			}
		}

		////////////////////////////////////////////////////////////////////////////////////////
		// front
		////////////////////////////////////////////////////////////////////////////////////////
		if (strcmp(cmd_ip, "front") == 0) {
			if (m_max == 0) {  //비어있으면
				printf("-1\n");
			}
			else {
				printf("%d\n", m_que[0]);
			}
		}

		//////////////////////////////////////////////////////////////////////////////////////////
		// back
		//////////////////////////////////////////////////////////////////////////////////////////
		if (strcmp(cmd_ip, "back") == 0) {
			if (m_max == 0) {  //비어있으면
				printf("-1\n");
			}
			else {
				printf("%d\n", m_que[m_max - 1]);
			}
		}

		///////////////////////////////////////////////////////////////////////////////////////////
		// empty
		///////////////////////////////////////////////////////////////////////////////////////////
		if (strcmp(cmd_ip, "empty") == 0) {
			if (m_max == 0) {  //비어있으면
				printf("1\n");
			}
			else {
				printf("0\n");
			}
		}

		///////////////////////////////////////////////////////////////////////////////////////////
		// size
		///////////////////////////////////////////////////////////////////////////////////////////
		if (strcmp(cmd_ip, "size") == 0) {
			printf("%d\n", m_max);
		}
	}

	return 0;
}
