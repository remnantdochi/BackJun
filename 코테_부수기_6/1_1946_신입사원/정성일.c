#include <stdio.h>
#include <stdlib.h>

int main() {
	int test_case;  //반복 횟수
	int n;  //사람 수
	int* rank;  //데이터 저장 리스트
	int low;  // 
	int cnt;  // 최대 합격수 카운트
	int i;  // 반복문용
	int temp1, temp2;  // 입력용

	//////////////////////////////////////////////////////
	// 테스트 횟수 입력
	//////////////////////////////////////////////////////
	scanf("%d", &test_case);

	//////////////////////////////////////////////////////
	// 테스트
	//////////////////////////////////////////////////////
	for (; test_case > 0; test_case--) {
		//////////////////////////////////////////////////////
		// 입력
		//////////////////////////////////////////////////////
		scanf("%d", &n);  // 사람수 입력
		rank = (int*)malloc(sizeof(int) * n);  // 변수 할당
		for (i = 0; i < n; i++) {
			scanf("%d %d", &temp1, &temp2);  // 순위 입력
			rank[temp1-1] = temp2;  // 1차서류순위 순서로 2차면접순위를 저장
		}

		//////////////////////////////////////////////////////
		// 초기화
		//////////////////////////////////////////////////////
		cnt = 1;  // 1차 1위는 합격
		low = rank[0];  // 2위가 넘어야하는 2차 순위는 1위 순위

		//////////////////////////////////////////////////////
		// 합불여부 판단
		//////////////////////////////////////////////////////
		for (i = 1; i < n; i++) {
			if (rank[i] < low) {  // 1차 상위자들중 가장 높은 2차순위와 비교
				low = rank[i];  // 다음사람이 넘어야 하는 2차순위 갱신
				cnt++;  // 합격자 +1
			}
		}

		//////////////////////////////////////////////////////
		// 결과 출력
		//////////////////////////////////////////////////////
		printf("%d\n", cnt);

		//////////////////////////////////////////////////////
		// 할당 해제
		//////////////////////////////////////////////////////
		free(rank);
	}

	return 0;
}
